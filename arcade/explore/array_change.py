def array_change(inputArray):
    moves = 0
    for i in range(len(inputArray) - 1):
        increment = 0
        if inputArray[i] >= inputArray[i+1]:
            increment += inputArray[i] - inputArray[i+1] + 1
            inputArray[i+1] += increment
            moves += increment
    
    return moves

print(array_change([1, 1, 1])) # 3
print(array_change([-1000, 0, -2, 0])) # 5
